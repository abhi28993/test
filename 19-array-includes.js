// Question: Check if an array includes a specific value.
// Write a function that returns true if the array includes the value, false otherwise.

function arrayIncludes(arr, value) {
  // Solution:
  return arr.includes(value);
}

// Example:
console.log(arrayIncludes([1, 2, 3], 2)); // true
console.log(arrayIncludes([1, 2, 3], 4)); // false 